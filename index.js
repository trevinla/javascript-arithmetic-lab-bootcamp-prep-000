function add(a,b) {
  var total = a + b;
  return total;
}

function subtract(a,b) {
  var total = a - b;
  return total;
}

function multiply(a,b) {
  var total = a * b;
  return total;
}

function divide(a,b) {
  var total = a / b;
  return total;
}

function inc(n) {
  n++;
  return n;
}

function dec(n) {
  n--;
  return n;
}

function makeInt(n, b) {
  parseInt(n, b);
  return n;
}

function preserveDecimal(n) {
  parseFloat(n);
  return n;
}
